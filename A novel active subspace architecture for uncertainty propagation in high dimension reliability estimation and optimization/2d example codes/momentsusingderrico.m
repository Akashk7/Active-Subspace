function [mu_h,std_h]=momentsusingderrico(t2l,t2b,W,sigma2,t1,lbx,ubx,psf)
W2 = W(:,2);
mu_t2 = (t2l+t2b)/2;
std_t2 = sum((W2.*sigma2').^2);
t21 = mu_t2-sqrt(3)*std_t2;
t22 = mu_t2;
t23 = mu_t2+sqrt(3)*std_t2;
x3 = [t1 t21;t1 t22;t1 t23]*W';
x3_denorm = denormalizeuv(x3,lbx,ubx);
Nmcs = 1e6;
Pftarget = 0.00135;
h = psf(x3_denorm,lbx,ubx,sigma2,Nmcs,Pftarget);
h1 = h(1);
h2 = h(2);
h3 = h(3);
w1 = 1/6;
w2 = 1/6;
w3 = 1/6;
mu_h = h1*w1+h2*w2+h3*w3;
std_h = w1*((h1-mu_h).^2)+w2*((h2-mu_h).^2)+w3*((h3-mu_h).^2);
