function y =  constraint3c(x)

x1=x(:,1);
x2=x(:,2);
x3=x(:,3);
x5=x(:,4);
x7=x(:,5);
p1=x(:,6);
p2=x(:,7);
p3=x(:,8);
 y1 = 33.86;
 y2 = 2.95*(x3);
 y3 = 0.1792*(p3);
 y4 = -5.057*(x1.*x2);
 y5 = -11*x2.*p1;
 y6 = -0.0215*x5.*p3;
 y7 = -9.98*x7.*p1;
 y8 =  22*p1.*p2;
y = 32./(y1+y2+y3+y4+y5+y6+y7+y8);
end